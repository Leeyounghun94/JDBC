package chap08.lineage.DTO;

public class PaladinDTO extends HumanDTO {
	
	
	
	
	//필드
	
		private String bluntweapon;		//팔라딘 무기 : 둔기
		private String armor;	//갑옷
		private String boots;	//부츠
		private String shield;	//방패
		
		
		
	//생성자
		
		
		
		
		
		
	//메서드
		
		
		public String getBluntweapon() {
			return bluntweapon;
		}
		public void setBluntweapon(String bluntweapon) {
			this.bluntweapon = bluntweapon;
		}
		public String getArmor() {
			return armor;
		}
		public void setArmor(String armor) {
			this.armor = armor;
		}
		public String getBoots() {
			return boots;
		}
		public void setBoots(String boots) {
			this.boots = boots;
		}
		public String getShield() {
			return shield;
		}
		public void setShield(String shield) {
			this.shield = shield;
		}
		
		
		
		
		

}
