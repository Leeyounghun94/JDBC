package chap01;

public class RunStateExam {

	public static void main(String[] args) {
		// int 정수 기입하는 방법 연습해보기
		
		int x = 50;     //x 변수에 50을 넣는다.
		int y = 777;    //y 변수에 777을 넣는다.
		int result = x * y ;	// result 변수에 x * y 값이 나온다.
		System.out.println(" x * y = " + result); // 결과값 출력함
		
		// println = 줄 바꿈으로 나타나는 형식.
		// print = 연속적으로 나오는 형식.

	}

}
